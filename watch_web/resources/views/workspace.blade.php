                <h3>MANUFACTURE</h3>
                <p>Founded in 1988, but only three years later, Frederique Constant created the "Heart Beat Manufacture" movement, featuring a reversed oscillator from the 12 o'clock to 6 o'clock position – considered a breakthrough in watchmaking design and granted a patent.

                    It wasn’t until 2004 that the brand introduced its first Manufacture watches to the market, beginning with the Manufacture Heart Beat watch, which won the “Watch of the Year” award from Horloges magazine.
                    
                    To improve the quality of the Manufacture timepieces, the company continually upgraded the movement to a higher level, such as using Silicon – a new material in watchmaking that is light, durable, absolutely anti-magnetic, creates no friction, and offers much higher accuracy than alloys previously used.
                    
                    Later, the Manufacture collection was further equipped with the highly intricate and sophisticated Tourbillon movement, incorporating even more advanced features.</p>
